#include<iostream>
using namespace std;
int main(){
    int a;
    cout<<"Enter the Number: "<<endl;
    cin>>a;
    if(a>0){
        cout<<"The number you have entered is POSITIVE"<<endl;

    }else if(a<0){
        cout<<"The number you have entered is NEGATIVE "<<endl;

    }else if(a==0){
        cout<<"The number you have entered is ZERO"<<endl;

    }else{
        cout<<"INVALID INPUT"<<endl;
    }
    return 0;


}
